    public void completarDatos(){
        ticketnum.setText("Ticket NÂ° " + ticket.getNumero());
        fecha.setText(ticket.getFecha());
        tipoVehiculo.setText(ticket.getPerteneceA().getClass().getSimpleName());
        patente.setText(ticket.getPerteneceA().getPatente());
        hEntrada.setText(ticket.getHoraEntrada());
        if(ticket.getHoraSalida()==null){
            hSalida.setText("-");
        }else{
            hSalida.setText(ticket.getHoraSalida());
        }
        tarifa.setText("$ " + ticket.getPerteneceA().getTarifa());  
        plaza.setText(String.valueOf(ticket.getEstacionadoEn().getNumero()));
        
    }
    public TicketPantalla(Ticket t) {
        this.ticket = t;
        initComponents();
        setTitle("Ticket");
        setLocationRelativeTo(null);
        setResizable(false);
        completarDatos();
       
    }

