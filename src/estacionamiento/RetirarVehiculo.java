    private void confirmarEgresoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmarEgresoActionPerformed
        for(int i = 0; i < Estacionamiento.getTickets().size(); i++){
            Ticket ticket = Estacionamiento.getTickets().get(i);
            if(ticket.getPerteneceA().getPatente().equals(patente.getText())){
                //Hora
                SimpleDateFormat hora = new SimpleDateFormat("HH:mm");
                String hSalida = hora.format(new Date());
                //System.out.println(hSalida);
                ticket.setHoraSalida(hSalida);
                TicketPantalla ticketP = new TicketPantalla(ticket);
                ticketP.setVisible(true);
                ticket.getEstacionadoEn().setEsta(Estacionamiento.getEstadoPlazaLibre());
                eliminarVehiculo(ticket);
                Estacionamiento.getTickets().remove(i);
                dispose();
    
            }
        }
        
        
    }//GEN-LAST:event_confirmarEgresoActionPerformed

