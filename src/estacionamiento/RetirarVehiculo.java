    public void eliminarVehiculo(Ticket t){
        if(tipoSeleccionado == "AUTO"){
            for(int i=0; i<Estacionamiento.getAutos().size(); i++){
                if(t.getPerteneceA().getPatente().equals(Estacionamiento.getAutos().get(i).getPatente())){
                    Estacionamiento.getAutos().remove(i);
                    System.out.println("Eliminado");
                }
            }
        }else{
            if(tipoSeleccionado=="CAMIONETA"){
                for(int i=0; i<Estacionamiento.getCamionetas().size();i++){
                    if(t.getPerteneceA().getPatente().equals(Estacionamiento.getCamionetas().get(i).getPatente())){
                        Estacionamiento.getAutos().remove(i);
                    }
                }
            }else{
                for(int i=0; i<Estacionamiento.getMotos().size();i++){
                    if(t.getPerteneceA().getPatente().equals(Estacionamiento.getMotos().get(i).getPatente())){
                        Estacionamiento.getMotos().remove(i);
                    }
                }
            }
        }
    }

